// Function to search cities by name
export const searchCities = (query) => {
  if (!query || query.length < 2) return [];
  
  const lowercaseQuery = query.toLowerCase();
  return indonesianCities
    .filter(city => 
      city.name.toLowerCase().includes(lowercaseQuery) ||
      city.province.toLowerCase().includes(lowercaseQuery)
    )
    .slice(0, 10); // Limit to 10 results
};

// Function to get all provinces
export const getProvinces = () => {
  const provinces = [...new Set(indonesianCities.map(city => city.province))];
  return provinces.sort();
};

// Function to get cities by province
export const getCitiesByProvince = (province) => {
  return indonesianCities.filter(city => city.province === province);
};
